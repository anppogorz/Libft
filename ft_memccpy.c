#include "libft.h"

void	*ft_memccpy(void *dst, const void *src, int c, size_t n)
{
	size_t i;

	i = 0;
	while (i < n)
	{
		if ((unsigned char)dst == (unsigned char)c)
			return (dst + i + 1);
		dst = (void *)src;
		i++;
	}
	return (NULL);
}
