#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int i;

	i = 0;
	while (s[i] != '\0')
		i++;
	while ((s[i] != (char)c && (i > 0)))
		i--;
	return ((char *)(s + i));
}
