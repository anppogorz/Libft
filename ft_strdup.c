#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int i;
	char	*s2;

	i = 0;
	while (s1[i] != '\0')
		i++;
	s2 = malloc(sizeof(s2) * i);
	if (s2 == 0)
		return (NULL);
	i = 0;
	while (s1[i] != '\0')
	{
		s2[i] = s1[i];
		i++;
	}
	return (s2);
}
