#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t i;

	i = 0;
	while (i < n)
	{
		if (((unsigned char)s == (unsigned char)c) && (i < n))
			return ((void *)(s + i));
		i++;
	}
	return (NULL);
}
